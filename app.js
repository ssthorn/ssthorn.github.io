





$(document).ready(function(){
  $("li").on({
      mouseover: function(){
          $(this).css("background-color", "white");
      },  
     
      mouseleave: function(){
          $(this).css("background-color", "rgb(10, 57, 73)");
      },
     
      mousedown: function(){
          $(this).css("background-color", "#cbf2a8");
      },

      mouseup: function(){
        $(this).css("background-color", "white");
      },
      
   
    
});






});